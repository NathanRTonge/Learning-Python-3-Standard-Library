# Math Module Part I
import math as m

# Constants
print(m.pi)
print(m.e, '\n')

# Not A Number and Infinity
print(m.nan)
print(m.inf) 
print(-m.inf, '\n')

# Trigonometry
print(m.cos(m.pi / 4))
print(m.sin(m.pi / 4))
print(m.sqrt(2)/2, '\n')

# Ceiling and Floor
num = 10.3
print(m.ceil(num)) #Always rounds up
print(m.floor(num)) #Always rounds down
