# Math Module Part 2
import math

# Factorial & Square Root
print(math.factorial(3)) #3!
print(math.sqrt(64), '\n')

# Greatest Common Denominator GCD
print(math.gcd(52, 8))
print(math.gcd(8, 52), '\n')

print(8/52)
print(2/13, '\n')

# Degrees and Radians
print(math.radians(360))
print(math.pi * 2)
print(math.degrees(math.pi * 2))